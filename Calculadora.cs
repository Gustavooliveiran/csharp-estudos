using System;

class Calculadora
{
    static void Main()
    {
        int num1 = 10;
        int num2 = 5;

        Console.WriteLine("Soma: " + (num1 + num2));
        Console.WriteLine("Subtração: " + (num1 - num2));
        Console.WriteLine("Multiplicação: " + (num1 * num2));
        Console.WriteLine("Divisão: " + (num1 / num2));
    }
}
